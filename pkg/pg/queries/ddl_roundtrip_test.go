func TestDDL_RoundTrip(t *testing.T) {
	for _, inst := range pgInstances {
		t.Run(fmt.Sprintf("pg%d", inst.version), func(t *testing.T) {
			ctx := context.Background()
			sourceDB := fmt.Sprintf("ddl_source_%d", inst.version)
			replayDB := fmt.Sprintf("ddl_replay_%d", inst.version)

			// 1. Create a dedicated source database with only our golden schema.
			_, _ = inst.admin.Exec(ctx, "DROP DATABASE IF EXISTS "+sourceDB)
			_, err := inst.admin.Exec(ctx, "CREATE DATABASE "+sourceDB)
			if err != nil {
				t.Fatalf("CREATE DATABASE %s failed: %v", sourceDB, err)
			}
			t.Cleanup(func() {
				_, _ = inst.admin.Exec(ctx, "DROP DATABASE IF EXISTS "+sourceDB)
			})

			sourceConnStr := replaceDBInConnStr(inst.admin, sourceDB)
			sourceAdmin, err := pgxpool.New(ctx, sourceConnStr)
			if err != nil {
				t.Fatalf("failed to connect to source DB: %v", err)
			}
			defer sourceAdmin.Close()

			// Set up golden schema fixtures in the source DB.
			for _, sql := range goldenSchemaFixtures {
				if _, err := sourceAdmin.Exec(ctx, sql); err != nil {
					t.Fatalf("fixture setup failed: %s\nerror: %v", sql, err)
				}
			}
			// Version-conditional fixtures.
			if inst.version >= 15 {
				for _, sql := range goldenSchemaFixturesPG15 {
					if _, err := sourceAdmin.Exec(ctx, sql); err != nil {
						t.Fatalf("PG15+ fixture setup failed: %s\nerror: %v", sql, err)
					}
				}
			}

			// Create pg_monitor role in source DB so CollectDDL works.
			for _, sql := range []string{
				`DO $$ BEGIN IF NOT EXISTS (SELECT FROM pg_roles WHERE rolname = 'monitor') THEN CREATE ROLE monitor LOGIN PASSWORD 'monitor'; END IF; END $$`,
				`GRANT pg_monitor TO monitor`,
			} {
				if _, err := sourceAdmin.Exec(ctx, sql); err != nil {
					t.Fatalf("monitor setup in source DB: %v", err)
				}
			}

			// Connect to source DB as pg_monitor.
			monitorConnStr := replaceDBInConnStr(inst.admin, sourceDB)
			monitorConnStr = strings.Replace(monitorConnStr, "user=test", "user=monitor", 1)
			monitorConnStr = strings.Replace(monitorConnStr, "password=test", "password=monitor", 1)
			sourceMonitor, err := pgxpool.New(ctx, monitorConnStr)
			if err != nil {
				t.Fatalf("failed to connect to source DB as monitor: %v", err)
			}
			defer sourceMonitor.Close()

			// 2. Collect DDL with our tool (as pg_monitor).
			ourDDL, err := CollectDDL(sourceMonitor, ctx)
			if err != nil {
				t.Fatalf("CollectDDL failed: %v", err)
			}
			if ourDDL == "" {
				t.Fatal("CollectDDL returned empty string")
			}

			// 3. Take catalog snapshot of source DB.
			sourceSnap, err := takeCatalogSnapshot(sourceAdmin, ctx)
			if err != nil {
				t.Fatalf("source catalog snapshot failed: %v", err)
			}

			// 4. Create fresh database and replay our DDL.
			_, _ = inst.admin.Exec(ctx, "DROP DATABASE IF EXISTS "+replayDB)
			_, err = inst.admin.Exec(ctx, "CREATE DATABASE "+replayDB)
			if err != nil {
				t.Fatalf("CREATE DATABASE %s failed: %v", replayDB, err)
			}
			t.Cleanup(func() {
				_, _ = inst.admin.Exec(ctx, "DROP DATABASE IF EXISTS "+replayDB)
			})

			replayConnStr := replaceDBInConnStr(inst.admin, replayDB)
			replayPool, err := pgxpool.New(ctx, replayConnStr)
			if err != nil {
				t.Fatalf("failed to connect to replay DB: %v", err)
			}
			defer replayPool.Close()

			// Replay our DDL.
			if _, err := replayPool.Exec(ctx, ourDDL); err != nil {
				t.Fatalf("replay of our DDL failed:\nerror: %v\nDDL:\n%s", err, ourDDL)
			}

			// 5. Catalog comparison: source vs replayed.
			replaySnap, err := takeCatalogSnapshot(replayPool, ctx)
			if err != nil {
				t.Fatalf("replay catalog snapshot failed: %v", err)
			}

			if diff := compareCatalogSnapshots(sourceSnap, replaySnap); diff != "" {
				t.Errorf("catalog mismatch between source and replayed DB:\n%s", diff)
			}

			// 6. pg_dump comparison: source vs replayed.
			sourceDump := pgDumpSchemaOnly(t, inst, sourceDB)
			replayDump := pgDumpSchemaOnly(t, inst, replayDB)

			if sourceDump != replayDump {
				t.Errorf("pg_dump output differs between source and replayed DB:\n%s",
					unifiedTextDiff(sourceDump, replayDump))
			}
		})
	}
}

// replaceDBInConnStr gets the connection config from an existing pool and
// replaces the database name.
func replaceDBInConnStr(pool *pgxpool.Pool, dbName string) string {
	cfg := pool.Config().ConnConfig.Copy()
	cfg.Database = dbName
	return fmt.Sprintf("host=%s port=%d user=%s password=%s dbname=%s sslmode=disable",
		cfg.Host, cfg.Port, cfg.User, cfg.Password, dbName)
}

// unifiedTextDiff produces a simple line-by-line diff between two strings.
func unifiedTextDiff(a, b string) string {
	linesA := strings.Split(a, "\n")
	linesB := strings.Split(b, "\n")

	var diff strings.Builder
	maxLen := len(linesA)
	if len(linesB) > maxLen {
		maxLen = len(linesB)
	}

	diffCount := 0
	for i := 0; i < maxLen; i++ {
		la, lb := "", ""
		if i < len(linesA) {
			la = linesA[i]
		}
		if i < len(linesB) {
			lb = linesB[i]
		}
		if la != lb {
			diffCount++
			if diffCount <= 50 { // cap output
				fmt.Fprintf(&diff, "line %d:\n  source:   %s\n  replayed: %s\n", i+1, la, lb)
			}
		}
	}
	if diffCount > 50 {
		fmt.Fprintf(&diff, "... and %d more differences\n", diffCount-50)
	}
	if diffCount == 0 && len(linesA) != len(linesB) {
		fmt.Fprintf(&diff, "line count differs: source=%d replayed=%d\n", len(linesA), len(linesB))
	}
	return diff.String()
}
