// ---------------------------------------------------------------------------
// Catalog snapshot — queries the same catalogs on two databases and compares.
// ---------------------------------------------------------------------------

// catalogSnapshot holds the key catalog state of a database schema.
type catalogSnapshot struct {
	Tables      []string // sorted "tablename|relkind|reloptions"
	Columns     []string // sorted "tablename|attnum|colname|typename|notnull|default|identity|generated"
	Constraints []string // sorted "tablename|conname|contype|condef"
	Indexes     []string // sorted "indexname|indexdef"
	Sequences   []string // sorted "seqname|datatype|start|increment|cycle"
	Types       []string // sorted "schema.typename|typtype"
	Functions   []string // sorted "funcname|funcdef"
	Triggers    []string // sorted "tablename|trigname|trigdef"
	Views       []string // sorted "viewname|viewdef"
	Policies    []string // sorted "tablename|polname|polcmd|permissive|qual|withcheck"
	ForeignKeys []string // sorted "tablename|conname|condef"
	Partitions  []string // sorted "child|parent|bound"
	Inherits    []string // sorted "child|parent"
}

func takeCatalogSnapshot(pool *pgxpool.Pool, ctx context.Context) (*catalogSnapshot, error) {
	snap := &catalogSnapshot{}
	var err error

	// Tables
	snap.Tables, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || c.relkind::text || '|' || COALESCE(array_to_string(c.reloptions, ','), '')
		FROM pg_class c JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND c.relkind IN ('r','p')
		ORDER BY c.relname`)
	if err != nil {
		return nil, fmt.Errorf("tables: %w", err)
	}

	// Columns
	snap.Columns, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || a.attnum || '|' || a.attname || '|' ||
		       format_type(a.atttypid, a.atttypmod) || '|' || a.attnotnull || '|' ||
		       COALESCE(pg_get_expr(d.adbin, d.adrelid), '') || '|' ||
		       COALESCE(a.attidentity::text, '') || '|' ||
		       COALESCE(a.attgenerated::text, '')
		FROM pg_attribute a
		JOIN pg_class c ON c.oid = a.attrelid
		JOIN pg_namespace n ON n.oid = c.relnamespace
		LEFT JOIN pg_attrdef d ON d.adrelid = a.attrelid AND d.adnum = a.attnum
		WHERE n.nspname = 'public' AND c.relkind IN ('r','p')
		  AND a.attnum > 0 AND NOT a.attisdropped
		ORDER BY c.relname, a.attnum`)
	if err != nil {
		return nil, fmt.Errorf("columns: %w", err)
	}

	// Constraints (non-FK)
	snap.Constraints, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || con.conname || '|' || con.contype::text || '|' ||
		       pg_get_constraintdef(con.oid)
		FROM pg_constraint con
		JOIN pg_class c ON c.oid = con.conrelid
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND con.contype IN ('p','u','c','x')
		ORDER BY c.relname, con.conname`)
	if err != nil {
		return nil, fmt.Errorf("constraints: %w", err)
	}

	// Foreign keys
	snap.ForeignKeys, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || con.conname || '|' || pg_get_constraintdef(con.oid)
		FROM pg_constraint con
		JOIN pg_class c ON c.oid = con.conrelid
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND con.contype = 'f'
		ORDER BY c.relname, con.conname`)
	if err != nil {
		return nil, fmt.Errorf("foreign keys: %w", err)
	}

	// Indexes (exclude constraint-backing indexes)
	snap.Indexes, err = queryStringSlice(pool, ctx, `
		SELECT indexname || '|' || indexdef
		FROM pg_indexes
		WHERE schemaname = 'public'
		  AND indexname NOT IN (
		      SELECT c.relname
		      FROM pg_constraint con
		      JOIN pg_class c ON c.oid = con.conindid
		      JOIN pg_namespace n ON n.oid = c.relnamespace
		      WHERE n.nspname = 'public'
		  )
		ORDER BY indexname`)
	if err != nil {
		return nil, fmt.Errorf("indexes: %w", err)
	}

	// Sequences (exclude identity-backing sequences)
	snap.Sequences, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || format_type(s.seqtypid, NULL) || '|' ||
		       s.seqstart || '|' || s.seqincrement || '|' || s.seqcycle
		FROM pg_class c
		JOIN pg_namespace n ON n.oid = c.relnamespace
		JOIN pg_sequence s ON s.seqrelid = c.oid
		WHERE n.nspname = 'public' AND c.relkind = 'S'
		  AND NOT EXISTS (
		      SELECT 1 FROM pg_depend d
		      WHERE d.objid = c.oid
		        AND d.deptype = 'i'
		        AND d.refclassid = 'pg_class'::regclass
		  )
		ORDER BY c.relname`)
	if err != nil {
		return nil, fmt.Errorf("sequences: %w", err)
	}

	// Types (enums + domains)
	snap.Types, err = queryStringSlice(pool, ctx, `
		SELECT quote_ident(tn.nspname) || '.' || quote_ident(t.typname) || '|' || t.typtype::text
		FROM pg_type t
		JOIN pg_namespace tn ON tn.oid = t.typnamespace
		WHERE t.typtype IN ('e','d')
		  AND t.oid IN (
		      SELECT DISTINCT a.atttypid FROM pg_attribute a
		      JOIN pg_class c ON c.oid = a.attrelid
		      JOIN pg_namespace n ON n.oid = c.relnamespace
		      WHERE n.nspname = 'public' AND c.relkind IN ('r','p')
		        AND a.attnum > 0 AND NOT a.attisdropped
		  )
		ORDER BY tn.nspname, t.typname`)
	if err != nil {
		return nil, fmt.Errorf("types: %w", err)
	}

	// Functions (exclude extension-owned)
	snap.Functions, err = queryStringSlice(pool, ctx, `
		SELECT p.proname || '|' || pg_get_functiondef(p.oid)
		FROM pg_proc p
		JOIN pg_namespace n ON n.oid = p.pronamespace
		WHERE n.nspname = 'public' AND p.prokind IN ('f','p')
		  AND NOT EXISTS (
		      SELECT 1 FROM pg_depend d
		      WHERE d.objid = p.oid AND d.deptype = 'e'
		  )
		ORDER BY p.proname, p.oid`)
	if err != nil {
		return nil, fmt.Errorf("functions: %w", err)
	}

	// Triggers
	snap.Triggers, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || t.tgname || '|' || pg_get_triggerdef(t.oid)
		FROM pg_trigger t
		JOIN pg_class c ON c.oid = t.tgrelid
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND NOT t.tgisinternal
		ORDER BY c.relname, t.tgname`)
	if err != nil {
		return nil, fmt.Errorf("triggers: %w", err)
	}

	// Views + materialized views
	snap.Views, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || c.relkind::text || '|' || pg_get_viewdef(c.oid)
		FROM pg_class c
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND c.relkind IN ('v','m')
		  AND NOT EXISTS (
		      SELECT 1 FROM pg_depend d
		      WHERE d.objid = c.oid AND d.deptype = 'e'
		  )
		ORDER BY c.relname`)
	if err != nil {
		return nil, fmt.Errorf("views: %w", err)
	}

	// RLS policies
	snap.Policies, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || pol.polname || '|' || pol.polcmd::text || '|' || pol.polpermissive || '|' ||
		       COALESCE(pg_get_expr(pol.polqual, pol.polrelid), '') || '|' ||
		       COALESCE(pg_get_expr(pol.polwithcheck, pol.polrelid), '')
		FROM pg_policy pol
		JOIN pg_class c ON c.oid = pol.polrelid
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public'
		ORDER BY c.relname, pol.polname`)
	if err != nil {
		return nil, fmt.Errorf("policies: %w", err)
	}

	// Partition relationships
	snap.Partitions, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || pc.relname || '|' || pg_get_expr(c.relpartbound, c.oid)
		FROM pg_inherits i
		JOIN pg_class c ON c.oid = i.inhrelid
		JOIN pg_class pc ON pc.oid = i.inhparent
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND pc.relkind = 'p'
		ORDER BY pc.relname, c.relname`)
	if err != nil {
		return nil, fmt.Errorf("partitions: %w", err)
	}

	// Inheritance (non-partition)
	snap.Inherits, err = queryStringSlice(pool, ctx, `
		SELECT c.relname || '|' || pc.relname
		FROM pg_inherits i
		JOIN pg_class c ON c.oid = i.inhrelid
		JOIN pg_class pc ON pc.oid = i.inhparent
		JOIN pg_namespace n ON n.oid = c.relnamespace
		WHERE n.nspname = 'public' AND pc.relkind = 'r'
		ORDER BY c.relname, pc.relname`)
	if err != nil {
		return nil, fmt.Errorf("inheritance: %w", err)
	}

	return snap, nil
}

// queryStringSlice runs a query and returns all rows as a sorted string slice.
func queryStringSlice(pool *pgxpool.Pool, ctx context.Context, query string) ([]string, error) {
	rows, err := pool.Query(ctx, query)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var result []string
	for rows.Next() {
		var s string
		if err := rows.Scan(&s); err != nil {
			return nil, err
		}
		result = append(result, s)
	}
	sort.Strings(result)
	return result, rows.Err()
}

// compareCatalogSnapshots compares two snapshots and returns a human-readable diff.
// Returns empty string if identical.
func compareCatalogSnapshots(source, replayed *catalogSnapshot) string {
	var diffs []string

	check := func(label string, src, rep []string) {
		d := diffStringSlices(label, src, rep)
		if d != "" {
			diffs = append(diffs, d)
		}
	}

	check("tables", source.Tables, replayed.Tables)
	check("columns", source.Columns, replayed.Columns)
	check("constraints", source.Constraints, replayed.Constraints)
	check("foreign_keys", source.ForeignKeys, replayed.ForeignKeys)
	check("indexes", source.Indexes, replayed.Indexes)
	check("sequences", source.Sequences, replayed.Sequences)
	check("types", source.Types, replayed.Types)
	check("functions", source.Functions, replayed.Functions)
	check("triggers", source.Triggers, replayed.Triggers)
	check("views", source.Views, replayed.Views)
	check("policies", source.Policies, replayed.Policies)
	check("partitions", source.Partitions, replayed.Partitions)
	check("inheritance", source.Inherits, replayed.Inherits)

	return strings.Join(diffs, "\n")
}

// diffStringSlices returns a human-readable diff of two sorted string slices.
func diffStringSlices(label string, a, b []string) string {
	setA := make(map[string]bool, len(a))
	setB := make(map[string]bool, len(b))
	for _, s := range a {
		setA[s] = true
	}
	for _, s := range b {
		setB[s] = true
	}

	var missing, extra []string
	for _, s := range a {
		if !setB[s] {
			missing = append(missing, s)
		}
	}
	for _, s := range b {
		if !setA[s] {
			extra = append(extra, s)
		}
	}

	if len(missing) == 0 && len(extra) == 0 {
		return ""
	}

	var b2 strings.Builder
	fmt.Fprintf(&b2, "[%s] mismatch:\n", label)
	for _, s := range missing {
		fmt.Fprintf(&b2, "  - (in source, not replayed): %s\n", s)
	}
	for _, s := range extra {
		fmt.Fprintf(&b2, "  + (in replayed, not source):  %s\n", s)
	}
	return b2.String()
}

