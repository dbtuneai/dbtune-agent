// pgDumpSchemaOnly runs pg_dump --schema-only inside the container and returns
// the normalized output.
func pgDumpSchemaOnly(t *testing.T, inst pgInstance, dbName string) string {
	t.Helper()
	ctx := context.Background()

	exitCode, reader, err := inst.container.Exec(ctx,
		[]string{
			"pg_dump",
			"--schema-only",
			"--no-owner",
			"--no-privileges",
			"--no-comments",
			"--no-security-labels",
			"--no-tablespaces",
			"--no-publications",
			"--no-subscriptions",
			"-d", dbName,
			"-U", "test",
		},
		tcexec.Multiplexed(),
	)
	if err != nil {
		t.Fatalf("pg_dump exec failed: %v", err)
	}

	output, err := io.ReadAll(reader)
	if err != nil {
		t.Fatalf("pg_dump read output failed: %v", err)
	}

	if exitCode != 0 {
		t.Fatalf("pg_dump exited %d: %s", exitCode, string(output))
	}

	return normalizePgDump(string(output))
}

// Patterns to strip from pg_dump output for comparison.
var pgDumpStripPatterns = []*regexp.Regexp{
	regexp.MustCompile(`(?m)^--.*$`),                                                       // all comments
	regexp.MustCompile(`(?m)^SET .*;\s*$`),                                                 // SET statements
	regexp.MustCompile(`(?m)^SELECT pg_catalog\..*;\s*$`),                                  // SELECT pg_catalog calls
	regexp.MustCompile(`(?m)^ALTER .* OWNER TO .*;\s*$`),                                   // ownership
	regexp.MustCompile(`(?m)^GRANT .*;\s*$`),                                               // grants
	regexp.MustCompile(`(?m)^REVOKE .*;\s*$`),                                              // revokes
	regexp.MustCompile(`(?m)^\\\.\s*$`),                                                    // COPY terminators
	regexp.MustCompile(`(?m)^\\restrict\s+.*$`),                                            // \restrict lines
	regexp.MustCompile(`(?m)^\\unrestrict\s+.*$`),                                          // \unrestrict lines
	regexp.MustCompile(`(?m)^CREATE EXTENSION .*;\s*$`),                                    // extension creation
	regexp.MustCompile(`(?m)^CREATE SCHEMA .*;\s*$`),                                       // non-public schema creation
	regexp.MustCompile(`(?ms)^CREATE TABLE private_schema\..*?;\s*$`),                      // non-public schema tables (multiline)
	regexp.MustCompile(`(?m)^ALTER SEQUENCE .* OWNED BY .*;\s*$`),                          // sequence ownership
	regexp.MustCompile(`(?m)^ALTER TABLE .* ALTER COLUMN .* SET DEFAULT nextval\(.*;\s*$`), // serial default setup
}

// normalizePgDump strips metadata, comments, SET statements, and collapses
// blank lines so two pg_dump outputs can be compared semantically.
func normalizePgDump(dump string) string {
	// Normalize line endings
	dump = strings.ReplaceAll(dump, "\r\n", "\n")

	// Strip patterns
	for _, re := range pgDumpStripPatterns {
		dump = re.ReplaceAllString(dump, "")
	}

	// Collapse multiple blank lines into one
	dump = regexp.MustCompile(`\n{3,}`).ReplaceAllString(dump, "\n\n")

	return strings.TrimSpace(dump)
}

